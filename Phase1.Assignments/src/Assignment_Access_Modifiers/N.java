package Assignment_Access_Modifiers;

public class N {
	protected long a = 6454636;
	protected long b = 23423553;
	protected int c = 45;
	protected int d = 23;
	protected double e = 23.45;
	protected double f = 12.67;
	
	protected long add () {
		return a + b;
	}
	
	public int subtract () {
		return c - d;
	}
	
	double multiply () {
		return e * f;
	}
}
